    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $palettes = Palette::with('colors')->where('user_id', $id)->get();
        foreach($palettes as $pal){
            $pal->colors()->detach();
